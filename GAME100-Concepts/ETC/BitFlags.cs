    class BitFlags {
        private ulong _flags;

        public bool isSet(FLAG value) {
            return (_flags & (ulong)value) > 0 ? true : false;
        }
        public void Set(FLAG value) {
            _flags |= (ulong)value;
        }
        public void UnSet(FLAG value){
            _flags &= (~(ulong)value);
        }
        
